/**
 * Export generated timetable to TXT, PDF, or DOCX.
 * Uses generatedResult from POST /api/school/timetable/generate/
 */

import { BREAK_AFTER_PERIOD } from './timetablePeriods';

function findEntryForClass(entries, classItem, periodKey, dayName) {
  return entries.find(
    (e) =>
      e.day === dayName &&
      e.period === periodKey &&
      e.grade === classItem.grade &&
      (e.section || '') === (classItem.section || '')
  );
}

function findEntryForTeacher(entries, teacherId, periodKey, dayName) {
  return entries.find((e) => {
    const tid = typeof e.teacher === 'object' ? e.teacher?.id : e.teacher;
    return e.day === dayName && String(tid) === String(teacherId) && e.period === periodKey;
  });
}

/**
 * Build the same `by_class` shape as the generator export, from the principal grid’s
 * current scope (day vs week, by class vs by teacher).
 */
export function buildLivePrincipalTimetableExport({
  viewBy,
  timeScope,
  masterDayParam,
  days,
  classRows,
  teacherRows,
  entries,
  periodsForClass,
  teacherPeriodsSlice,
  getTeacherName,
  labels = {},
}) {
  const dayList =
    timeScope === 'week'
      ? [...days]
      : masterDayParam && days.includes(masterDayParam)
        ? [masterDayParam]
        : [];

  const by_class = {};

  const slotsForDay = (periods, dayName, pickEntry) => {
    const slots = [];
    for (const period of periods) {
      if (period.key === BREAK_AFTER_PERIOD) {
        slots.push({ type: 'break', label: labels.break || 'Break' });
      }
      const entry = pickEntry(period.key, dayName);
      if (entry) {
        const tn = getTeacherName(entry);
        const slot = {
          type: 'class',
          label: period.key,
          subject: entry.subject || '-',
          teacher_name: tn || undefined,
        };
        if (viewBy === 'teacher') {
          const grade = entry.grade != null ? String(entry.grade).trim() : '';
          const sec = entry.section != null ? String(entry.section).trim() : '';
          const classLabel =
            grade || sec ? `${grade}${sec ? `-${sec}` : ''}` : undefined;
          if (classLabel) slot.class_label = classLabel;
        }
        slots.push(slot);
      }
    }
    return slots;
  };

  if (viewBy === 'class') {
    for (const c of classRows) {
      const label = `${c.grade}${c.section ? `-${c.section}` : ''}`;
      const periods = periodsForClass(c);
      by_class[label] = dayList.map((dayName) => ({
        day: dayName,
        slots: slotsForDay(periods, dayName, (pk, dn) => findEntryForClass(entries, c, pk, dn)),
      }));
    }
  } else {
    for (const t of teacherRows) {
      const label = t.name || `Teacher ${t.id}`;
      by_class[label] = dayList.map((dayName) => ({
        day: dayName,
        slots: slotsForDay(teacherPeriodsSlice, dayName, (pk, dn) =>
          findEntryForTeacher(entries, t.id, pk, dn)
        ),
      }));
    }
  }

  return { by_class };
}

/**
 * Build plain text representation of the timetable (by class).
 * @param {{ by_class: Record<string, Array<{ day: string, slots: Array<{ type: string, label: string, subject?: string, teacher_name?: string }> }> }} generatedResult
 * @returns {string}
 */
export function timetableToText(generatedResult, options = {}) {
  if (!generatedResult?.by_class) return '';
  const labels = {
    classPrefix: options.labels?.classPrefix || 'Class',
    breakLabel: options.labels?.breakLabel || 'Break',
    generatedOn: options.labels?.generatedOn || 'Generated on',
    generatedBy: options.labels?.generatedBy || 'Generated by',
  };
  const lines = [];
  if (options.metadata?.generatedBy) {
    lines.push(
      `${labels.generatedBy}: ${options.metadata.generatedBy} · ${labels.generatedOn}: ${new Date().toLocaleString()}`
    );
    lines.push('');
  }
  for (const [classKey, daySlots] of Object.entries(generatedResult.by_class)) {
    lines.push(`${labels.classPrefix} ${classKey}`);
    lines.push('');
    for (const { day, slots } of daySlots) {
      lines.push(`${day}:`);
      const parts = slots.map((slot) => {
        if (slot.type === 'break') return slot.label || labels.breakLabel;
        const cls = slot.class_label ? ` [${slot.class_label}]` : '';
        return `${slot.label}: ${slot.subject || '-'}${cls}${slot.teacher_name ? ` (${slot.teacher_name})` : ''}`;
      });
      lines.push(parts.join(', '));
      lines.push('');
    }
    lines.push('---');
    lines.push('');
  }
  return lines.join('\n').trim();
}

/**
 * Download timetable as TXT file.
 */
export function exportTimetableTxt(generatedResult, filename = 'timetable.txt', options = {}) {
  const text = timetableToText(generatedResult, options);
  const blob = new Blob([text], { type: 'text/plain;charset=utf-8' });
  const url = URL.createObjectURL(blob);
  const a = document.createElement('a');
  a.href = url;
  a.download = filename;
  a.click();
  URL.revokeObjectURL(url);
}

